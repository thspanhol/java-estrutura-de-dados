public class ListaFilaPilha {/*

### Lista

- Definição: Uma lista é uma coleção ordenada de elementos onde cada elemento pode ser acessado por sua posição (índice).
  As listas são dinâmicas, ou seja, podem aumentar ou diminuir de tamanho conforme necessário.

- Características:
  - Acesso Direto: Permite acessar qualquer elemento diretamente pelo seu índice.
  - Flexibilidade: Permite adicionar, remover e modificar elementos em qualquer posição.
  - Ordenação: Mantém os elementos em uma ordem específica.

### Fila

- Definição: Uma fila é uma coleção de elementos que segue o princípio FIFO (First In, First Out), ou seja,
  o primeiro elemento a ser inserido é o primeiro a ser removido.

- Características:
  - Inserção no Fim: Novos elementos são adicionados no final da fila.
  - Remoção do Início: Elementos são removidos do início da fila.
  - Acesso Sequencial: Não permite acesso direto a elementos específicos; apenas o primeiro elemento pode ser removido diretamente.

### Pilha

- Definição: Uma pilha é uma coleção de elementos que segue o princípio LIFO (Last In, First Out), ou seja,
  o último elemento a ser inserido é o primeiro a ser removido.

- Características:
  - Inserção e Remoção no Topo: Tanto a inserção quanto a remoção de elementos ocorrem no topo da pilha.
  - Acesso Restrito: Só é possível acessar o elemento no topo da pilha diretamente.
  - Navegação: É ideal para rastrear estados de execução, como em chamadas de função recursivas.

### Resumo das Diferenças

- Lista: Acesso aleatório, modificável em qualquer posição.
- Fila: Inserção no fim, remoção no início, FIFO.
- Pilha: Inserção e remoção no topo, LIFO.

*/}
